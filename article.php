    <!-- Script for AJAX -->
    <script>
        $(document).ready(function() {
            load_data();
        });

        function load_data(page) {
            $.ajax({
                url: "articles/get.php",
                method: "GET",
                data: {
                    p: page
                },
                success: function(data) {
                    $('#article_data').html(data);
                }
            });
        }
    </script>

